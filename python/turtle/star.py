import turtle
t=turtle.Turtle()
w=turtle.Screen()
w.title('STAR')
w.bgcolor('black')
t.up()
t.goto(0,-100)
t.down()
t.pencolor('white')
t.pensize(2)
t.begin_fill()
t.fillcolor('yellow')
t.circle(200,None,3)
t.up()
t.left(90)
t.forward(400)
t.left(90)
t.down()
t.circle(200,None,3)
t.end_fill()
w.exitonclick()